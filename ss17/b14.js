let r = parseFloat(prompt("Nhập bán kính của hình trụ (đơn vị: cm):"));
let h = parseFloat(prompt("Nhập chiều cao của hình trụ (đơn vị: cm) :"));
let dienTichXungQuanh = 2 * Math.PI * r * h;
let dienTichToanPhan = 2 * Math.PI * r * (r + h);
let theTich = Math.PI * Math.pow(r, 2) * h;
let chuViDay = 2 * Math.PI * r;
document.write("Diện tích xung quanh: " + dienTichXungQuanh.toFixed(2) + "<br>");
document.write("Diện tích toàn phần: " + dienTichToanPhan.toFixed(2) + " <br>");
document.write("Thể tích hình trụ: " + theTich.toFixed(2) + "<br>");
document.write("Chu vi đáy: " + chuViDay.toFixed(2) + " <br>");